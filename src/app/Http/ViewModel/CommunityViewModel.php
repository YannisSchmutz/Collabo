<?php


namespace App\Http\ViewModel;


class CommunityViewModel
{
    private $projects;

    /**
     * @return mixed
     */
    public function getProjects()
    {
        return $this->projects;
    }

    /**
     * @param mixed $projects
     */
    public function setProjects($projects): void
    {
        $this->projects = $projects;
    }
}
